    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        try{
        Usuario[] aux = new Usuario[ListaUsuarios.getLista().length -1];
                int row = tabla.getSelectedRow();
                
                if (ListaUsuarios.getLista()[row] != null) {
                    ListaUsuarios.getLista()[row] = null;
                }
                for (int i = 0; i < ListaUsuarios.getLista().length; i++ ) {
                    if (ListaUsuarios.getLista()[i] !=null) {
                        for (int j = 0; j < aux.length; j++) {
                            if (aux[j] == null) {
                                aux[j] = ListaUsuarios.getLista()[i];
                                
                                break;
                            }
                        }
                        
                    }
                }
                ListaUsuarios.setLista(aux);
                
                refreshTable();
                
           ListaUsuarios.saveLista();
        
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "No se ha seleccionado nada");
        }

    }//GEN-LAST:event_jButton4ActionPerformed

