private void refreshTable() {
       
       Usuario[] listado = ListaUsuarios.getLista();
       
      
       

        DefaultTableModel model = new DefaultTableModel() {
           
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        model.addColumn("");
        model.addColumn("Nombre");
        model.addColumn("Apellido");
        model.addColumn("Dni");
        model.addColumn("Plan");
        model.addColumn("Dias");
        model.addColumn("Fecha de Ingreso");
       
        for (int i = 0; i < listado.length; i++) {
            if (listado[i] != null) {
                String[] fila = new String[7];
                fila[0] = "" + (i + 1);
                fila[1] = listado[i].getNombre();
                fila[2] = listado[i].getApellido();
                fila[3] = "" + listado[i].getDni();
                fila[4] = listado[i].getNivelSelec();
                fila[5] = "" + listado[i].getDiasSem();
                fila[6] = listado[i].getFechaIngreso();
                
                model.addRow(fila);
                 
            }
        }
        tabla.setModel(model);
    }

 
 
